package datatypes;

public class CastingDatatypes {
	public static void main(String[] args) {
		// inital values
		int i = 200;
			//cast to short
		short s = (short)i;
		System.out.println(s);
		
			//cast to double
		double d = (double)i;
		System.out.println(d);
		
			//cast to a byte
		byte b = (byte)i;
		System.out.println(b);
	}
}
