package datatypes;

public class MathOperators {
	public static void main(String[] args) {
		int x = 5;
		int y = 3;
		int z;
		
		
		// Addition 
		z = x + y;
		System.out.println(z);
		
		//Subtraction		
		z = x - y;
		System.out.println(z);
		
		//Multiplication
		z = x * y;
		System.out.println(z);
		
		//Division
		z = x / y;
		System.out.println(z);
		
		//Modulus
		z = x % y;
		System.out.println(z);
		
		// Float vs Int
		float f = 3.6f;
		int i =22;
		double result = f + i;
		System.out.println(result);
	}
}
